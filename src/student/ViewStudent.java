     public  byte[] convertImageIconToByteArray(Icon icon) {
//         A BufferedImage in Java is a type of image representation in memory
        BufferedImage bufferedImage = new BufferedImage(
                icon.getIconWidth(),
                icon.getIconHeight(),
                BufferedImage.TYPE_INT_RGB);
        
        // Paint the ImageIcon to the BufferedImage
        icon.paintIcon(null, bufferedImage.getGraphics(), 0, 0);
        
        // Convert BufferedImage to byte[]
        ByteArrayOutputStream baos = new ByteArrayOutputStream();
        try {
//            to write an image to output stream
            ImageIO.write(bufferedImage, "png", baos); // Change format as needed
        } catch (Exception ex) {
            ex.printStackTrace();
        }
        
        return baos.toByteArray();
    }
    
    
